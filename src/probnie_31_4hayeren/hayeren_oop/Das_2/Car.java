package probnie_31_4hayeren.hayeren_oop.Das_2;

public class Car {
    private String number;
    private int year;


    public Car(String n) {
        number = n;

    }

    public Car() {

    }


    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

}

